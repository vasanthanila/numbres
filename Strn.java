public class Strn {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        String s=sc.nextLine();
        int i=Integer.parseInt(s);
        System.out.println(i);
    }
    
}
